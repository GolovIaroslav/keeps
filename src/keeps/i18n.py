def normalize_language(code: str) -> str:
    supported = {language_code for language_code, _label in SUPPORTED_LANGUAGES}
    return code if code in supported else "en"


def translate_text(language: str, source_text: str) -> str:
    return _TRANSLATIONS.get(normalize_language(language), {}).get(source_text, source_text)


class KeepsTranslator(QTranslator):
    def __init__(self, language: str) -> None:
        super().__init__()
        self.language = normalize_language(language)

    def translate(self, _context, source_text, _disambiguation=None, _n=-1):
        return translate_text(self.language, source_text)


_active_translator: KeepsTranslator | None = None


def install_translator(application, language: str) -> KeepsTranslator | None:
    """Install the requested catalog, keeping English as the source fallback."""
    global _active_translator
    if _active_translator is not None:
        application.removeTranslator(_active_translator)
        _active_translator = None
    language = normalize_language(language)
    if language == "en":
        return None
    _active_translator = KeepsTranslator(language)
    application.installTranslator(_active_translator)
    return _active_translator
